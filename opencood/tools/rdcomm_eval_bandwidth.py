def run_one_setting(
    args: argparse.Namespace,
    tau_c: float,
    tau_mi: float,
    output_root: str,
) -> Tuple[Dict[str, Any], int]:
    """
    Run one threshold setting and return row + return_code.
    """
    name = run_name(tau_c, tau_mi)
    output_dir = ensure_dir(os.path.join(output_root, name))
    summary_path = os.path.join(output_dir, "rdcomm_inference_summary.json")
    log_path = os.path.join(output_dir, "run.log")

    if args.skip_existing and os.path.isfile(summary_path):
        summary = read_json(summary_path)
        row = extract_summary_row(
            summary=summary,
            tau_c=tau_c,
            tau_mi=tau_mi,
            output_dir=output_dir,
            return_code=0,
            elapsed_sec=0.0,
        )
        row["skipped_existing"] = True
        print(f"[RDcomm sweep] skip existing: {name}")
        return row, 0

    cmd = build_inference_command(
        args=args,
        tau_c=tau_c,
        tau_mi=tau_mi,
        output_dir=output_dir,
    )

    print("\n[RDcomm sweep] run:")
    print(f"  name: {name}")
    print(f"  output_dir: {output_dir}")
    print(f"  command: {command_to_string(cmd)}")

    if args.dry_run:
        row = {
            "tau_c": float(tau_c),
            "tau_mi": float(tau_mi),
            "output_dir": output_dir,
            "command": command_to_string(cmd),
            "return_code": 0,
            "dry_run": True,
        }
        return row, 0

    start = time.time()
    return_code = run_command(
        cmd=cmd,
        log_path=log_path,
        quiet=bool(args.quiet),
    )
    elapsed = time.time() - start

    if return_code != 0:
        row = {
            "tau_c": float(tau_c),
            "tau_mi": float(tau_mi),
            "output_dir": output_dir,
            "return_code": int(return_code),
            "elapsed_sec": float(elapsed),
            "log_path": log_path,
            "error": "subprocess_failed",
        }
        save_json(row, os.path.join(output_dir, "rdcomm_inference_failed.json"))
        return row, return_code

    if not os.path.isfile(summary_path):
        row = {
            "tau_c": float(tau_c),
            "tau_mi": float(tau_mi),
            "output_dir": output_dir,
            "return_code": int(return_code),
            "elapsed_sec": float(elapsed),
            "log_path": log_path,
            "error": "summary_not_found",
        }
        return row, 2

    summary = read_json(summary_path)

    row = extract_summary_row(
        summary=summary,
        tau_c=tau_c,
        tau_mi=tau_mi,
        output_dir=output_dir,
        return_code=return_code,
        elapsed_sec=elapsed,
    )
    row["log_path"] = log_path
    row["skipped_existing"] = False

    return row, 0


def sort_rows(rows: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Sort rows by tau_mi then tau_c.
    """
    return sorted(
        rows,
        key=lambda r: (
            float(r.get("tau_mi", 0.0)),
            float(r.get("tau_c", 0.0)),
        ),
    )


def print_table(rows: Sequence[Mapping[str, Any]]) -> None:
    """
    Print compact result table.
    """
    if len(rows) == 0:
        print("[RDcomm sweep] no rows.")
        return

    candidate_ap_keys = [
        "AP30",
        "AP50",
        "AP70",
        "ap_results_AP30",
        "ap_results_AP50",
        "ap_results_AP70",
    ]

    print("\n[RDcomm sweep] compact results")
    header = (
        f"{'tau_c':>10} "
        f"{'tau_mi':>10} "
        f"{'AP30':>10} "
        f"{'AP50':>10} "
        f"{'AP70':>10} "
        f"{'comm_KB':>12} "
        f"{'sel_ratio':>12} "
        f"{'ret':>5}"
    )
    print(header)
    print("-" * len(header))

    for row in rows:
        ap30 = row.get("AP30", row.get("ap_results_AP30", ""))
        ap50 = row.get("AP50", row.get("ap_results_AP50", ""))
        ap70 = row.get("AP70", row.get("ap_results_AP70", ""))

        comm_kb = row.get(
            "comm_total_KB",
            row.get(
                "comm_comm_KB",
                row.get("comm_avg_KB_per_batch", ""),
            ),
        )

        sel_ratio = row.get(
            "comm_avg_final_selected_ratio",
            row.get("comm_final_selected_ratio", ""),
        )

        def fmt(value: Any, width: int = 10) -> str:
            try:
                return f"{float(value):>{width}.6f}"
            except Exception:
                return f"{str(value):>{width}}"

        print(
            f"{fmt(row.get('tau_c', ''), 10)} "
            f"{fmt(row.get('tau_mi', ''), 10)} "
            f"{fmt(ap30, 10)} "
            f"{fmt(ap50, 10)} "
            f"{fmt(ap70, 10)} "
            f"{fmt(comm_kb, 12)} "
            f"{fmt(sel_ratio, 12)} "
            f"{int(row.get('return_code', 0)):>5}"
        )



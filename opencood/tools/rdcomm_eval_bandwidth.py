def build_inference_command(
    args: argparse.Namespace,
    tau_c: float,
    tau_mi: float,
    output_dir: str,
) -> List[str]:
    """
    Build command for inference_rdcomm.py.
    """
    script = get_inference_script(args)

    cmd = [
        args.python,
        script,
        "--hypes_yaml",
        args.hypes_yaml,
        "--checkpoint",
        args.checkpoint,
        "--output_dir",
        output_dir,
        "--rdcomm_stage",
        args.rdcomm_stage,
        "--tau_c",
        str(float(tau_c)),
        "--tau_mi",
        str(float(tau_mi)),
        "--ap_thresholds",
        args.ap_thresholds,
        "--max_batches",
        str(int(args.max_batches)),
        "--seed",
        str(int(args.seed)),
    ]

    if args.huffman_table:
        cmd.extend(["--huffman_table", args.huffman_table])

    if args.batch_size is not None:
        cmd.extend(["--batch_size", str(int(args.batch_size))])

    if args.num_workers is not None:
        cmd.extend(["--num_workers", str(int(args.num_workers))])

    if args.global_sort_detections:
        cmd.append("--global_sort_detections")

    if args.data_parallel:
        cmd.append("--data_parallel")

    if args.no_cuda:
        cmd.append("--no_cuda")

    if args.strict_load:
        cmd.append("--strict_load")

    if args.no_ignore_shape_mismatch:
        cmd.append("--no_ignore_shape_mismatch")

    if args.save_predictions:
        cmd.append("--save_predictions")

    if args.save_per_sample_csv:
        cmd.append("--save_per_sample_csv")

    if args.extra_args:
        cmd.extend(shlex.split(args.extra_args))

    return cmd


def run_command(
    cmd: Sequence[str],
    log_path: str,
    quiet: bool = False,
) -> int:
    """
    Run subprocess while writing stdout/stderr to log file.
    """
    ensure_parent_dir(log_path)

    with open(log_path, "w", encoding="utf-8") as log_file:
        log_file.write(command_to_string(cmd) + "\n\n")
        log_file.flush()

        process = subprocess.Popen(
            list(cmd),
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            text=True,
            bufsize=1,
        )

        assert process.stdout is not None

        for line in process.stdout:
            log_file.write(line)
            log_file.flush()

            if not quiet:
                print(line, end="")

        process.wait()
        return int(process.returncode)



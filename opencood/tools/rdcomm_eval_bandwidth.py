def ensure_dir(path: str) -> str:
    os.makedirs(path, exist_ok=True)
    return path


def ensure_parent_dir(path: str) -> str:
    parent = os.path.dirname(os.path.abspath(path))
    if parent:
        os.makedirs(parent, exist_ok=True)
    return path


def parse_float_values(spec: str) -> List[float]:
    """
    Parse threshold spec.

    Supported:
        "0,0.001,0.01"
        "0 0.001 0.01"
        "0:0.1:0.01"   inclusive range
    """
    spec = str(spec).strip()

    if not spec:
        return []

    if ":" in spec and "," not in spec and ";" not in spec and " " not in spec:
        parts = spec.split(":")
        if len(parts) != 3:
            raise ValueError(
                f"Range spec must be start:end:step, got {spec!r}."
            )

        start, end, step = [float(v) for v in parts]

        if step == 0:
            raise ValueError("Range step cannot be 0.")

        values = []
        value = start

        if step > 0:
            while value <= end + 1e-12:
                values.append(value)
                value += step
        else:
            while value >= end - 1e-12:
                values.append(value)
                value += step

        return values

    normalized = spec.replace(";", ",").replace(" ", ",")
    values = []

    for item in normalized.split(","):
        item = item.strip()
        if item:
            values.append(float(item))

    return values


def make_sweep_pairs(
    tau_c_values: Sequence[float],
    tau_mi_values: Sequence[float],
    paired: bool = False,
) -> List[Tuple[float, float]]:
    """
    Build threshold pairs.
    """
    tau_c_values = list(tau_c_values)
    tau_mi_values = list(tau_mi_values)

    if len(tau_c_values) == 0:
        raise ValueError("tau_c_values is empty.")

    if len(tau_mi_values) == 0:
        raise ValueError("tau_mi_values is empty.")

    if paired:
        if len(tau_c_values) != len(tau_mi_values):
            raise ValueError(
                "--paired requires equal number of tau_c_values and tau_mi_values. "
                f"Got {len(tau_c_values)} and {len(tau_mi_values)}."
            )
        return list(zip(tau_c_values, tau_mi_values))

    pairs = []
    for tau_mi in tau_mi_values:
        for tau_c in tau_c_values:
            pairs.append((tau_c, tau_mi))

    return pairs


def format_float_for_name(value: float) -> str:
    """
    Format float for directory names.
    """
    text = f"{float(value):.10g}"

    if "e" in text or "E" in text:
        text = f"{float(value):.10f}".rstrip("0").rstrip(".")

    text = text.replace("-", "m").replace(".", "p")
    return text


def run_name(tau_c: float, tau_mi: float) -> str:
    return f"tau_c_{format_float_for_name(tau_c)}__tau_mi_{format_float_for_name(tau_mi)}"


def get_inference_script(args: argparse.Namespace) -> str:
    if args.inference_script:
        return args.inference_script

    return os.path.join(os.path.dirname(os.path.abspath(__file__)), "inference_rdcomm.py")


def command_to_string(cmd: Sequence[str]) -> str:
    return " ".join(shlex.quote(str(c)) for c in cmd)


def read_json(path: str) -> Dict[str, Any]:
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def save_json(data: Any, path: str, indent: int = 2) -> str:
    ensure_parent_dir(path)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=indent, ensure_ascii=False)
    return path


def write_csv(rows: Sequence[Mapping[str, Any]], path: str) -> str:
    ensure_parent_dir(path)

    if len(rows) == 0:
        with open(path, "w", encoding="utf-8") as f:
            f.write("")
        return path

    fieldnames = sorted(set(k for row in rows for k in row.keys()))

    with open(path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()

        for row in rows:
            writer.writerow(row)

    return path


def flatten_dict(
    data: Mapping[str, Any],
    prefix: str = "",
    out: Optional[Dict[str, Any]] = None,
) -> Dict[str, Any]:
    """
    Flatten nested dict for CSV row.
    """
    if out is None:
        out = {}

    for key, value in data.items():
        key = str(key)
        new_key = f"{prefix}_{key}" if prefix else key

        if isinstance(value, Mapping):
            flatten_dict(value, prefix=new_key, out=out)
        elif isinstance(value, (int, float, str, bool)) or value is None:
            out[new_key] = value
        else:
            try:
                out[new_key] = float(value)
            except Exception:
                out[new_key] = str(value)

    return out


def extract_summary_row(
    summary: Mapping[str, Any],
    tau_c: float,
    tau_mi: float,
    output_dir: str,
    return_code: int = 0,
    elapsed_sec: float = 0.0,
) -> Dict[str, Any]:
    """
    Convert inference summary into one CSV row.
    """
    row: Dict[str, Any] = {
        "tau_c": float(tau_c),
        "tau_mi": float(tau_mi),
        "output_dir": output_dir,
        "return_code": int(return_code),
        "elapsed_sec": float(elapsed_sec),
    }

    ap_results = summary.get("ap_results", {})
    if isinstance(ap_results, Mapping):
        for key, value in ap_results.items():
            row[str(key)] = value

    comm_summary = summary.get("comm_summary", {})
    if isinstance(comm_summary, Mapping):
        for key, value in comm_summary.items():
            row[f"comm_{key}"] = value

    # Keep useful metadata.
    for key in (
        "num_processed_batches",
        "checkpoint",
        "huffman_table",
        "hypes_yaml",
    ):
        if key in summary:
            row[key] = summary[key]

    return flatten_dict(row)



def main() -> None:
    args = parse_args()

    output_root = ensure_dir(args.output_root)

    tau_c_values = parse_float_values(args.tau_c_values)
    tau_mi_values = parse_float_values(args.tau_mi_values)

    pairs = make_sweep_pairs(
        tau_c_values=tau_c_values,
        tau_mi_values=tau_mi_values,
        paired=bool(args.paired),
    )

    print("[RDcomm sweep] configuration")
    print(f"  hypes_yaml: {args.hypes_yaml}")
    print(f"  checkpoint: {args.checkpoint}")
    print(f"  huffman_table: {args.huffman_table}")
    print(f"  output_root: {output_root}")
    print(f"  tau_c_values: {tau_c_values}")
    print(f"  tau_mi_values: {tau_mi_values}")
    print(f"  num_runs: {len(pairs)}")
    print(f"  inference_script: {get_inference_script(args)}")

    all_rows: List[Dict[str, Any]] = []
    failed_rows: List[Dict[str, Any]] = []

    sweep_start = time.time()

    for run_idx, (tau_c, tau_mi) in enumerate(pairs):
        print(
            f"\n[RDcomm sweep] [{run_idx + 1}/{len(pairs)}] "
            f"tau_c={tau_c}, tau_mi={tau_mi}"
        )

        row, return_code = run_one_setting(
            args=args,
            tau_c=float(tau_c),
            tau_mi=float(tau_mi),
            output_root=output_root,
        )

        all_rows.append(row)

        if return_code != 0:
            failed_rows.append(row)

            print(
                f"[RDcomm sweep] failed: tau_c={tau_c}, tau_mi={tau_mi}, "
                f"return_code={return_code}"
            )

            if not args.continue_on_error:
                print("[RDcomm sweep] stop because --continue_on_error is not set.")
                break

        # Save partial results every run.
        sorted_rows = sort_rows(all_rows)

        write_csv(
            sorted_rows,
            os.path.join(output_root, "rdcomm_bandwidth_sweep.csv"),
        )

        save_json(
            {
                "rows": sorted_rows,
                "failed_rows": failed_rows,
                "config": vars(args),
                "tau_c_values": tau_c_values,
                "tau_mi_values": tau_mi_values,
                "pairs": [{"tau_c": c, "tau_mi": m} for c, m in pairs],
                "elapsed_sec": time.time() - sweep_start,
            },
            os.path.join(output_root, "rdcomm_bandwidth_sweep.json"),
        )

    all_rows = sort_rows(all_rows)

    csv_path = write_csv(
        all_rows,
        os.path.join(output_root, "rdcomm_bandwidth_sweep.csv"),
    )

    json_path = save_json(
        {
            "rows": all_rows,
            "failed_rows": failed_rows,
            "config": vars(args),
            "tau_c_values": tau_c_values,
            "tau_mi_values": tau_mi_values,
            "pairs": [{"tau_c": c, "tau_mi": m} for c, m in pairs],
            "elapsed_sec": time.time() - sweep_start,
            "num_runs": len(all_rows),
            "num_failed": len(failed_rows),
        },
        os.path.join(output_root, "rdcomm_bandwidth_sweep.json"),
    )

    print_table(all_rows)

    print("\n[RDcomm sweep] finished")
    print(f"  csv: {csv_path}")
    print(f"  json: {json_path}")
    print(f"  failed: {len(failed_rows)} / {len(all_rows)}")


if __name__ == "__main__":
    main()
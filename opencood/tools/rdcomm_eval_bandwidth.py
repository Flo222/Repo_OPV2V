"""
Batch bandwidth evaluation for RDcomm.

This script sweeps RDcomm communication thresholds and repeatedly calls:

    opencood/tools/inference_rdcomm.py

It collects AP and communication statistics from each run's
rdcomm_inference_summary.json, then writes:

    1. rdcomm_bandwidth_sweep.csv
    2. rdcomm_bandwidth_sweep.json

Recommended usage:

    python opencood/tools/rdcomm_eval_bandwidth.py \
      --hypes_yaml opencood/hypes_yaml/rdcomm/point_pillar_rdcomm_opv2v_infer.yaml \
      --checkpoint path/to/stage3/net_epochXX.pth \
      --huffman_table path/to/stage2/huffman_table.pth \
      --output_root opencood/logs/rdcomm_bandwidth/opv2v \
      --tau_c_values "0,0.001,0.003,0.005,0.007,0.01,0.03,0.05,0.07,0.1" \
      --tau_mi_values "0.7"

Debug quick run:

    python opencood/tools/rdcomm_eval_bandwidth.py \
      --hypes_yaml xxx.yaml \
      --checkpoint xxx.pth \
      --huffman_table huffman_table.pth \
      --output_root debug_eval \
      --tau_c_values "0,0.01" \
      --tau_mi_values "0.7" \
      --max_batches 5
"""


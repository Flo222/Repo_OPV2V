def save_json(data: Mapping[str, Any], path: str) -> str:
    def convert(obj: Any) -> Any:
        if isinstance(obj, torch.Tensor):
            if obj.numel() == 1:
                return obj.detach().cpu().item()
            return obj.detach().cpu().tolist()

        if isinstance(obj, np.ndarray):
            return obj.tolist()

        if isinstance(obj, Mapping):
            return {str(k): convert(v) for k, v in obj.items()}

        if isinstance(obj, list):
            return [convert(v) for v in obj]

        if isinstance(obj, tuple):
            return [convert(v) for v in obj]

        return obj

    parent = os.path.dirname(os.path.abspath(path))
    if parent:
        os.makedirs(parent, exist_ok=True)

    with open(path, "w", encoding="utf-8") as f:
        json.dump(convert(data), f, indent=2, ensure_ascii=False)

    return path


def save_prediction_item(
    output_dir: str,
    batch_idx: int,
    pred_box_tensor: Optional[torch.Tensor],
    pred_score: Optional[torch.Tensor],
    gt_box_tensor: Optional[torch.Tensor],
    output_dict: Mapping[str, Any],
) -> None:
    pred_dir = os.path.join(output_dir, "predictions")
    os.makedirs(pred_dir, exist_ok=True)

    item = {
        "pred_box_tensor": tensor_to_cpu(pred_box_tensor),
        "pred_score": tensor_to_cpu(pred_score),
        "gt_box_tensor": tensor_to_cpu(gt_box_tensor),
        "comm_stats": tensor_to_cpu(output_dict.get("comm_stats", {})),
    }

    torch.save(item, os.path.join(pred_dir, f"batch_{batch_idx:06d}.pth"))



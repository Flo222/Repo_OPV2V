def post_process_batch(
    dataset: Any,
    batch_data: Mapping[str, Any],
    output_dict: Mapping[str, Any],
) -> Tuple[Optional[torch.Tensor], Optional[torch.Tensor], Optional[torch.Tensor]]:
    """
    Run OpenCOOD post_processor.post_process.

    Returns:
        pred_box_tensor, pred_score, gt_box_tensor
    """
    if not hasattr(dataset, "post_processor"):
        raise AttributeError("Dataset does not have post_processor.")

    post_processor = dataset.post_processor

    post_input = batch_data["ego"] if "ego" in batch_data else batch_data

    try:
        return post_processor.post_process(post_input, output_dict)
    except Exception:
        # Some versions expect the full batch_data.
        return post_processor.post_process(batch_data, output_dict)


def init_result_stat(thresholds: Sequence[float]) -> Dict[float, Dict[str, Any]]:
    return {
        float(th): {
            "tp": [],
            "fp": [],
            "gt": 0,
            "score": [],
        }
        for th in thresholds
    }


def update_result_stat(
    result_stat: Dict[float, Dict[str, Any]],
    pred_box_tensor: Optional[torch.Tensor],
    pred_score: Optional[torch.Tensor],
    gt_box_tensor: Optional[torch.Tensor],
    thresholds: Sequence[float],
) -> None:
    """
    Update OpenCOOD-style result_stat.
    """
    if eval_utils is not None and hasattr(eval_utils, "caluclate_tp_fp"):
        for thresh in thresholds:
            eval_utils.caluclate_tp_fp(
                pred_box_tensor,
                pred_score,
                gt_box_tensor,
                result_stat,
                float(thresh),
            )
        return

    if eval_utils is not None and hasattr(eval_utils, "calculate_tp_fp"):
        for thresh in thresholds:
            eval_utils.calculate_tp_fp(
                pred_box_tensor,
                pred_score,
                gt_box_tensor,
                result_stat,
                float(thresh),
            )
        return

    raise ImportError(
        "Cannot find eval_utils.caluclate_tp_fp / calculate_tp_fp. "
        "Please use your OpenCOOD eval_utils or add the function."
    )


def calculate_ap_from_stat_single(stat: Mapping[str, Any]) -> float:
    """
    Fallback AP calculation from result_stat[threshold].
    """
    tp = np.array(stat.get("tp", []), dtype=np.float32)
    fp = np.array(stat.get("fp", []), dtype=np.float32)
    scores = np.array(stat.get("score", []), dtype=np.float32)
    gt = int(stat.get("gt", 0))

    if gt <= 0 or tp.size == 0:
        return 0.0

    if scores.size == tp.size:
        order = np.argsort(-scores)
        tp = tp[order]
        fp = fp[order]

    tp_cum = np.cumsum(tp)
    fp_cum = np.cumsum(fp)

    recalls = tp_cum / max(float(gt), 1.0)
    precisions = tp_cum / np.maximum(tp_cum + fp_cum, 1e-12)

    # VOC-style integral AP.
    mrec = np.concatenate(([0.0], recalls, [1.0]))
    mpre = np.concatenate(([0.0], precisions, [0.0]))

    for i in range(mpre.size - 1, 0, -1):
        mpre[i - 1] = np.maximum(mpre[i - 1], mpre[i])

    changing_points = np.where(mrec[1:] != mrec[:-1])[0]
    ap = np.sum((mrec[changing_points + 1] - mrec[changing_points]) * mpre[changing_points + 1])

    return float(ap)


def final_eval_results(
    result_stat: Dict[float, Dict[str, Any]],
    output_dir: str,
    global_sort_detections: bool = False,
) -> Dict[str, float]:
    """
    Use OpenCOOD final eval when available, otherwise fallback.
    """
    if eval_utils is not None and hasattr(eval_utils, "eval_final_results"):
        try:
            ret = eval_utils.eval_final_results(
                result_stat,
                output_dir,
                global_sort_detections=global_sort_detections,
            )
        except TypeError:
            try:
                ret = eval_utils.eval_final_results(result_stat, output_dir)
            except TypeError:
                ret = eval_utils.eval_final_results(result_stat)

        if isinstance(ret, Mapping):
            return {str(k): float(v) for k, v in ret.items() if isinstance(v, (int, float))}

        if isinstance(ret, tuple):
            out: Dict[str, float] = {}
            sorted_thresholds = sorted(result_stat.keys())
            for idx, value in enumerate(ret):
                if idx < len(sorted_thresholds):
                    out[f"AP{int(sorted_thresholds[idx] * 100)}"] = float(value)
                else:
                    out[f"AP_{idx}"] = float(value)
            return out

    ap_dict = {}
    for thresh, stat in result_stat.items():
        ap_dict[f"AP{int(float(thresh) * 100)}"] = calculate_ap_from_stat_single(stat)

    return ap_dict



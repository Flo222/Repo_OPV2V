"""
Inference script for RDcomm on OpenCOOD.

This script evaluates PointPillar-RDcomm under the full communication-efficient
inference pipeline:

    BEV feature
      -> confidence selection Mc
      -> layered VQ indices Dbase/Dres
      -> task entropy coding bit accounting
      -> MI selection MMI
      -> smoothing UNet
      -> RDcomm fusion
      -> detection head
      -> OpenCOOD post-processing / AP evaluation

Recommended command:

    python opencood/tools/inference_rdcomm.py \
      --hypes_yaml opencood/hypes_yaml/rdcomm/point_pillar_rdcomm_opv2v_infer.yaml \
      --checkpoint path/to/stage3/net_epochXX.pth \
      --huffman_table path/to/stage2/huffman_table.pth \
      --tau_c 0.005 \
      --tau_mi 0.7 \
      --output_dir opencood/logs/rdcomm_eval/opv2v_tau0005

Batch sweep is handled by tools/rdcomm_eval_bandwidth.py.
"""


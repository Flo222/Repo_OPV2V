def extract_comm_stats(output_dict: Mapping[str, Any]) -> Dict[str, float]:
    """
    Extract communication stats from model output.
    """
    stats: Dict[str, float] = {}

    comm_stats = output_dict.get("comm_stats", None)

    if isinstance(comm_stats, Mapping):
        for key, value in comm_stats.items():
            if isinstance(value, (int, float, torch.Tensor)):
                stats[str(key)] = to_float(value)

    for key in ("comm_bits", "comm_KB", "comm_MB"):
        if key in output_dict:
            stats[key] = to_float(output_dict[key])

    # Ensure canonical fields exist.
    if "total_bits" not in stats and "comm_bits" in stats:
        stats["total_bits"] = stats["comm_bits"]

    if "total_KB" not in stats and "comm_KB" in stats:
        stats["total_KB"] = stats["comm_KB"]

    if "total_MB" not in stats and "comm_MB" in stats:
        stats["total_MB"] = stats["comm_MB"]

    return stats


def aggregate_comm_stats(stats_list: Sequence[Mapping[str, float]]) -> Dict[str, float]:
    """
    Aggregate communication stats over all batches.
    """
    if len(stats_list) == 0:
        return {
            "total_bits": 0.0,
            "total_KB": 0.0,
            "total_MB": 0.0,
            "avg_bits_per_batch": 0.0,
            "avg_KB_per_batch": 0.0,
            "avg_MB_per_batch": 0.0,
            "avg_final_selected_ratio": 0.0,
            "avg_confidence_selected_ratio": 0.0,
            "avg_mi_selected_ratio_within_confidence": 0.0,
            "num_batches": 0.0,
        }

    sum_keys = (
        "total_bits",
        "total_bytes",
        "total_KB",
        "total_MB",
        "selected_message_bits",
        "selected_message_KB",
        "abstract_bits",
        "abstract_KB",
        "num_messages",
    )

    mean_keys = (
        "final_selected_ratio",
        "confidence_selected_ratio",
        "mi_selected_ratio_within_confidence",
    )

    out: Dict[str, float] = {}

    for key in sum_keys:
        out[key] = float(sum(float(s.get(key, 0.0)) for s in stats_list))

    n = max(len(stats_list), 1)

    out["avg_bits_per_batch"] = out.get("total_bits", 0.0) / n
    out["avg_KB_per_batch"] = out.get("total_KB", 0.0) / n
    out["avg_MB_per_batch"] = out.get("total_MB", 0.0) / n

    for key in mean_keys:
        out[f"avg_{key}"] = float(sum(float(s.get(key, 0.0)) for s in stats_list)) / n

    out["num_batches"] = float(len(stats_list))
    return out


def write_comm_csv(
    rows: Sequence[Mapping[str, Any]],
    path: str,
) -> str:
    if len(rows) == 0:
        return path

    fieldnames = sorted(set(k for row in rows for k in row.keys()))

    with open(path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow(row)

    return path



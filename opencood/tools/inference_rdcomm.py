def set_model_stage(model: torch.nn.Module, stage: str) -> None:
    core = get_core_model(model)

    if hasattr(core, "set_rdcomm_stage"):
        core.set_rdcomm_stage(stage)
    elif hasattr(core, "rdcomm_stage"):
        core.rdcomm_stage = stage


def set_model_thresholds(
    model: torch.nn.Module,
    tau_c: Optional[float],
    tau_mi: Optional[float],
) -> None:
    core = get_core_model(model)

    if hasattr(core, "set_thresholds"):
        core.set_thresholds(tau_c=tau_c, tau_mi=tau_mi)
        return

    if tau_c is not None and hasattr(core, "tau_c"):
        core.tau_c = float(tau_c)

    if tau_mi is not None and hasattr(core, "tau_mi"):
        core.tau_mi = float(tau_mi)


def load_model_checkpoint(
    model: torch.nn.Module,
    checkpoint: str,
    device: torch.device,
    strict: bool,
    ignore_shape_mismatch: bool,
) -> Dict[str, Any]:
    core = get_core_model(model)

    return load_state_dict_flexible(
        model=core,
        checkpoint_or_state_dict=checkpoint,
        map_location=device,
        strict=bool(strict),
        ignore_shape_mismatch=bool(ignore_shape_mismatch),
        verbose=True,
    )


def load_huffman_table_if_needed(
    model: torch.nn.Module,
    table_path: str,
) -> None:
    if not table_path:
        print("[RDcomm infer] no huffman_table provided; model will use current/fixed code lengths.")
        return

    core = get_core_model(model)

    if hasattr(core, "load_entropy_table"):
        core.load_entropy_table(table_path, strict_size=True)
        print(f"[RDcomm infer] huffman table loaded: {table_path}")
        return

    if hasattr(core, "entropy_coder"):
        core.entropy_coder.load_table(table_path, strict_size=True)
        print(f"[RDcomm infer] huffman table loaded: {table_path}")
        return

    raise AttributeError(
        "Model has no load_entropy_table() or entropy_coder. "
        "Please check point_pillar_rdcomm.py."
    )



def main() -> None:
    args = parse_args()

    set_random_seed(args.seed)

    device = get_device(no_cuda=bool(args.no_cuda))
    output_dir = resolve_output_dir(args)

    print(f"[RDcomm infer] device: {device}")
    print(f"[RDcomm infer] output_dir: {output_dir}")

    hypes = load_yaml(args)
    hypes = update_hypes_for_inference(hypes, args)

    dataset, loader = build_test_loader(args, hypes)

    model = create_model(hypes)
    model = model.to(device)

    stage = str(args.rdcomm_stage).lower()
    if stage == "inference":
        stage = "infer"

    set_model_stage(model, stage)

    load_model_checkpoint(
        model=model,
        checkpoint=args.checkpoint,
        device=device,
        strict=bool(args.strict_load),
        ignore_shape_mismatch=not bool(args.no_ignore_shape_mismatch),
    )

    load_huffman_table_if_needed(model, args.huffman_table)

    set_model_thresholds(
        model=model,
        tau_c=args.tau_c,
        tau_mi=args.tau_mi,
    )

    configure_inference_mode(
        get_core_model(model),
        freeze_all_params=True,
        verbose=True,
    )

    if args.data_parallel and torch.cuda.device_count() > 1:
        print(f"[RDcomm infer] using DataParallel on {torch.cuda.device_count()} GPUs")
        model = torch.nn.DataParallel(model)

    summary = run_inference(
        model=model,
        dataset=dataset,
        loader=loader,
        device=device,
        args=args,
        output_dir=output_dir,
    )

    print_summary(summary)
    print("[RDcomm infer] done.")


if __name__ == "__main__":
    main()
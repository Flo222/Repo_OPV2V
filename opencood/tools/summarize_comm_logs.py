def resolve_log_dir(opt: argparse.Namespace) -> Path:
    """
    Resolve log directory from --log_dir or --model_dir.
    """
    if opt.log_dir is not None:
        return Path(opt.log_dir).resolve()

    if opt.model_dir is not None:
        return (Path(opt.model_dir).resolve() / "arce_comm_logs")

    raise ValueError("Either --log_dir or --model_dir should be provided.")


def resolve_output_dir(opt: argparse.Namespace, log_dir: Path) -> Path:
    """
    Resolve output directory.
    """
    if opt.output_dir is not None:
        return ensure_dir(Path(opt.output_dir).resolve())

    return ensure_dir(log_dir)


def resolve_input_path(opt: argparse.Namespace, log_dir: Path) -> Tuple[Path, str]:
    """
    Resolve input JSONL path and guessed type.

    Returns
    -------
    tuple
        (path, guessed_type)
    """
    prefix = opt.prefix

    if opt.input_jsonl is not None:
        path = Path(opt.input_jsonl).resolve()
        guessed = "auto" if opt.input_type == "auto" else opt.input_type
        return path, guessed

    raw_path = log_dir / f"{prefix}{RAW_JSONL_SUFFIX}"
    flat_path = log_dir / f"{prefix}{FLAT_JSONL_SUFFIX}"

    if opt.prefer_flat:
        if flat_path.exists():
            return flat_path, "flat"
        if raw_path.exists():
            return raw_path, "raw"
    else:
        if raw_path.exists():
            return raw_path, "raw"
        if flat_path.exists():
            return flat_path, "flat"

    raise FileNotFoundError(
        "Could not find ARCE communication jsonl input. Tried:\n"
        f"  raw:  {raw_path}\n"
        f"  flat: {flat_path}\n"
        "You can also pass --input_jsonl explicitly."
    )


def infer_record_type(records: Sequence[Dict[str, Any]], fallback: str = "auto") -> str:
    """
    Infer whether records are raw nested records or flat metric records.

    Returns
    -------
    str
        raw or flat
    """
    if fallback in ("raw", "flat"):
        return fallback

    if not records:
        return "flat"

    sample = records[0]

    raw_keys = (
        "size",
        "channel",
        "action",
        "packetization",
        "quantization",
        "fec_decode",
        "partial_reconstruction",
    )

    if any(key in sample for key in raw_keys):
        return "raw"

    flat_keys = (
        METRIC_TRANSMITTED_BYTES,
        METRIC_PACKET_LOSS_RATIO,
        METRIC_CHANNEL_STATE,
        METRIC_QUANT_MODE,
        METRIC_FEC_TYPE,
    )

    if any(key in sample for key in flat_keys):
        return "flat"

    # Conservative fallback:
    # raw records usually have nested dictionaries; flat metrics usually do not.
    if any(isinstance(v, dict) for v in sample.values()):
        return "raw"

    return "flat"



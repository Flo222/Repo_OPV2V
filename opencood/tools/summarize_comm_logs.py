def write_extra_group_summaries(
    stats: CommStats,
    output_dir: Path,
    prefix: str,
    group_keys: Sequence[str],
) -> Dict[str, str]:
    """
    Write additional grouped summary JSON files.
    """
    written: Dict[str, str] = {}

    for key in group_keys:
        key = str(key).strip()
        if not key:
            continue

        summary = stats.summarize_by_metric_key(key)

        safe_key = key.replace("/", "_").replace(".", "_")
        out_path = output_dir / f"{prefix}_by_{safe_key}_from_logs.json"

        write_json(out_path, summary)
        written[key] = str(out_path)

    return written



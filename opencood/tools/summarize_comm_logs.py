"""
Offline communication-log summarizer for ARCE / OpenCOOD experiments.

This script reads ARCE communication logs generated by:

    opencood/tools/inference_arce.py
    opencood.comm.metrics.comm_logger.CommLogger

and produces offline summaries.

Supported input files:
    1. Raw nested records:
        arce_comm_records.jsonl

    2. Flat metric records:
        arce_comm_flat.jsonl

Preferred input:
    arce_comm_records.jsonl

If raw records are available, this script extracts flat metrics again and
summarizes them. If only flat records are available, it summarizes those
directly.

Typical usage:

    python opencood/tools/summarize_comm_logs.py \\
      --log_dir opencood/logs/xxx/arce_comm_logs

or:

    python opencood/tools/summarize_comm_logs.py \\
      --model_dir opencood/logs/xxx

Outputs:
    <prefix>_summary_from_logs.json
        Compact + overall summary.

    <prefix>_summary_full_from_logs.json
        Overall + by-frame + by-channel + by-quant + by-FEC summary.

    <prefix>_flat_from_logs.csv
        Flat per-link metric table.

    <prefix>_group_summary_from_logs.csv
        One CSV containing overall / by-frame / by-channel / by-quant / by-FEC
        summaries.

    <prefix>_report_from_logs.md
        Human-readable markdown report.

This script does NOT:
    - rerun inference;
    - modify model outputs;
    - compute detection AP;
    - simulate communication.

It only summarizes already saved ARCE communication logs.
"""


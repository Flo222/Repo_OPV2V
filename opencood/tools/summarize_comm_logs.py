def flatten_summary_row(
    group_type: str,
    group_key: str,
    summary: Dict[str, Any],
) -> Dict[str, Any]:
    """
    Flatten one summary into one CSV row.
    """
    row = {
        "group_type": group_type,
        "group_key": group_key,
    }

    flat = flatten_dict(summary, sep=".", max_depth=None)

    for key, value in flat.items():
        row[key] = value

    return row


def build_group_summary_rows(full_summary: Dict[str, Any]) -> List[Dict[str, Any]]:
    """
    Build one table containing overall and grouped summaries.
    """
    rows: List[Dict[str, Any]] = []

    overall = full_summary.get("overall", {}) or {}
    rows.append(flatten_summary_row("overall", "all", overall))

    group_map = {
        "frame": full_summary.get("by_frame", {}) or {},
        "channel_state": full_summary.get("by_channel_state", {}) or {},
        "quant_mode": full_summary.get("by_quant_mode", {}) or {},
        "fec_type": full_summary.get("by_fec_type", {}) or {},
    }

    for group_type, group_dict in group_map.items():
        for group_key, summary in group_dict.items():
            rows.append(
                flatten_summary_row(
                    group_type=group_type,
                    group_key=str(group_key),
                    summary=summary,
                )
            )

    return rows


def format_float(value: Any, digits: int = 6) -> str:
    """
    Format float-like value for markdown.
    """
    try:
        value = float(value)
    except (TypeError, ValueError):
        return str(value)

    if abs(value) >= 1000:
        return f"{value:.2f}"

    return f"{value:.{digits}f}"


def markdown_table(rows: Sequence[Tuple[str, Any]]) -> str:
    """
    Build a simple two-column markdown table.
    """
    lines = [
        "| Metric | Value |",
        "| --- | ---: |",
    ]

    for key, value in rows:
        if isinstance(value, float):
            value = format_float(value)
        lines.append(f"| {key} | {value} |")

    return "\n".join(lines)


def extract_compact_for_report(summary: Dict[str, Any]) -> List[Tuple[str, Any]]:
    """
    Extract compact report rows.
    """
    compact = summary.get("compact", {}) or {}

    return [
        ("num_records", compact.get("num_records", 0)),
        ("num_non_bypassed_records", compact.get("num_non_bypassed_records", 0)),
        ("total_transmitted_mb", compact.get("total_transmitted_mb", 0.0)),
        ("total_received_mb", compact.get("total_received_mb", 0.0)),
        ("total_source_packets", compact.get("total_source_packets", 0)),
        ("total_encoded_packets", compact.get("total_encoded_packets", 0)),
        ("total_lost_packets", compact.get("total_lost_packets", 0)),
        ("overall_packet_loss_ratio", compact.get("overall_packet_loss_ratio", 0.0)),
        ("total_fec_recovered_packets", compact.get("total_fec_recovered_packets", 0)),
        ("total_temporal_filled_packets", compact.get("total_temporal_filled_packets", 0)),
        ("total_spatial_filled_packets", compact.get("total_spatial_filled_packets", 0)),
        ("total_zero_filled_packets", compact.get("total_zero_filled_packets", 0)),
        ("total_still_missing_packets", compact.get("total_still_missing_packets", 0)),
        ("mean_packet_loss_ratio", compact.get("mean_packet_loss_ratio", 0.0)),
        ("mean_recovery_ratio", compact.get("mean_recovery_ratio", 0.0)),
    ]


def build_group_brief_rows(group_summary: Dict[str, Any]) -> List[Tuple[str, Any]]:
    """
    Build compact rows for one group summary.
    """
    sums = group_summary.get("sum", {}) or {}
    means = group_summary.get("mean", {}) or {}

    return [
        ("num_records", group_summary.get("num_records", 0)),
        ("num_non_bypassed_records", group_summary.get("num_non_bypassed_records", 0)),
        ("total_transmitted_mb", group_summary.get("total_transmitted_mb", 0.0)),
        ("total_received_mb", group_summary.get("total_received_mb", 0.0)),
        ("overall_packet_loss_ratio", group_summary.get("overall_packet_loss_ratio", 0.0)),
        ("sum_fec_recovered", sums.get(METRIC_NUM_FEC_RECOVERED, 0)),
        ("sum_temporal_filled", sums.get(METRIC_NUM_TEMPORAL_FILLED, 0)),
        ("sum_spatial_filled", sums.get(METRIC_NUM_SPATIAL_FILLED, 0)),
        ("sum_zero_filled", sums.get(METRIC_NUM_ZERO_FILLED, 0)),
        ("mean_recovery_ratio", means.get(f"mean_{METRIC_RECOVERY_RATIO}", 0.0)),
    ]


def build_markdown_report(
    input_path: Path,
    record_type: str,
    num_input_records: int,
    summary_payload: Dict[str, Any],
    full_summary: Dict[str, Any],
) -> str:
    """
    Build human-readable markdown report.
    """
    lines: List[str] = []

    lines.append("# ARCE Communication Log Summary")
    lines.append("")
    lines.append(f"- Input: `{input_path}`")
    lines.append(f"- Input type: `{record_type}`")
    lines.append(f"- Input records: `{num_input_records}`")
    lines.append("")

    lines.append("## Overall compact summary")
    lines.append("")
    lines.append(markdown_table(extract_compact_for_report(summary_payload)))
    lines.append("")

    by_channel = full_summary.get("by_channel_state", {}) or {}
    if by_channel:
        lines.append("## By channel state")
        lines.append("")
        for key, item in by_channel.items():
            lines.append(f"### `{key}`")
            lines.append("")
            lines.append(markdown_table(build_group_brief_rows(item)))
            lines.append("")

    by_quant = full_summary.get("by_quant_mode", {}) or {}
    if by_quant:
        lines.append("## By quantization mode")
        lines.append("")
        for key, item in by_quant.items():
            lines.append(f"### `{key}`")
            lines.append("")
            lines.append(markdown_table(build_group_brief_rows(item)))
            lines.append("")

    by_fec = full_summary.get("by_fec_type", {}) or {}
    if by_fec:
        lines.append("## By FEC type")
        lines.append("")
        for key, item in by_fec.items():
            lines.append(f"### `{key}`")
            lines.append("")
            lines.append(markdown_table(build_group_brief_rows(item)))
            lines.append("")

    lines.append("## Notes")
    lines.append("")
    lines.append(
        "- `source_packets` are original spatial feature packets."
    )
    lines.append(
        "- `encoded_packets` include source packets plus FEC parity / repair packets."
    )
    lines.append(
        "- GE packet loss acts on encoded packets, not only source packets."
    )
    lines.append(
        "- FEC / temporal / spatial / zero-fill counts describe how missing source packets were handled after channel loss."
    )
    lines.append("")

    return "\n".join(lines)


def print_compact_summary(compact: Dict[str, Any]) -> None:
    """
    Print compact summary.
    """
    msg = (
        "[ARCE summarize_comm_logs] "
        f"records={compact.get('num_records', 0)} "
        f"non_bypassed={compact.get('num_non_bypassed_records', 0)} "
        f"tx={compact.get('total_transmitted_mb', 0.0):.4f}MB "
        f"rx={compact.get('total_received_mb', 0.0):.4f}MB "
        f"loss={compact.get('overall_packet_loss_ratio', 0.0):.4f} "
        f"fec_rec={compact.get('total_fec_recovered_packets', 0)} "
        f"tmp={compact.get('total_temporal_filled_packets', 0)} "
        f"spatial={compact.get('total_spatial_filled_packets', 0)} "
        f"zero={compact.get('total_zero_filled_packets', 0)}"
    )
    print(msg)



def build_stats(
    records: Sequence[Dict[str, Any]],
    record_type: str,
    skip_bypassed: bool = False,
) -> CommStats:
    """
    Build CommStats from raw or flat records.
    """
    stats = CommStats(
        records=None,
        skip_bypassed=skip_bypassed,
        keep_records=True,
        keep_flat_metrics=True,
    )

    if record_type == "raw":
        stats.update(records)
        return stats

    if record_type == "flat":
        for item in records:
            stats.add_metrics(item)
        return stats

    raise ValueError(f"Unsupported record_type: {record_type}")


def get_flat_records(
    records: Sequence[Dict[str, Any]],
    record_type: str,
    skip_bypassed: bool = False,
) -> List[Dict[str, Any]]:
    """
    Return flat metric records.
    """
    flat_records: List[Dict[str, Any]] = []

    if record_type == "raw":
        for record in records:
            metrics = extract_record_metrics(record)

            if skip_bypassed and bool(metrics.get("bypassed", False)):
                continue

            flat_records.append(json_safe(metrics))

        return flat_records

    if record_type == "flat":
        for metrics in records:
            if skip_bypassed and bool(metrics.get("bypassed", False)):
                continue

            flat_records.append(json_safe(metrics))

        return flat_records

    raise ValueError(f"Unsupported record_type: {record_type}")



# Main
# ----------------------------------------------------------------------

def main(opt: argparse.Namespace) -> None:
    """
    Main entry.
    """
    log_dir = resolve_log_dir(opt)
    output_dir = resolve_output_dir(opt, log_dir)

    input_path, guessed_type = resolve_input_path(opt, log_dir)

    records = load_jsonl(input_path)

    record_type = infer_record_type(
        records,
        fallback=opt.input_type if opt.input_type != "auto" else guessed_type,
    )

    stats = build_stats(
        records=records,
        record_type=record_type,
        skip_bypassed=opt.skip_bypassed,
    )

    compact = stats.get_compact_summary()
    overall = stats.summarize()
    full_summary = stats.full_summary().as_dict()

    summary_payload = {
        "compact": compact,
        "overall": overall,
        "input": {
            "path": str(input_path),
            "type": record_type,
            "num_records": int(len(records)),
            "log_dir": str(log_dir),
            "skip_bypassed": bool(opt.skip_bypassed),
        },
    }

    prefix = opt.prefix

    summary_path = output_dir / f"{prefix}{SUMMARY_OUT_SUFFIX}"
    full_summary_path = output_dir / f"{prefix}{FULL_SUMMARY_OUT_SUFFIX}"
    flat_csv_path = output_dir / f"{prefix}{FLAT_CSV_OUT_SUFFIX}"
    group_csv_path = output_dir / f"{prefix}{GROUP_SUMMARY_CSV_SUFFIX}"
    report_path = output_dir / f"{prefix}{REPORT_OUT_SUFFIX}"

    write_json(summary_path, summary_payload)
    write_json(full_summary_path, full_summary)

    should_write_flat_csv = bool(opt.write_flat_csv or opt.all_outputs)
    should_write_group_csv = bool(opt.write_group_csv or opt.all_outputs)
    should_write_report = bool(opt.write_report or opt.all_outputs)

    if should_write_flat_csv:
        flat_records = get_flat_records(
            records=records,
            record_type=record_type,
            skip_bypassed=opt.skip_bypassed,
        )
        write_csv(flat_csv_path, flat_records)

    if should_write_group_csv:
        group_rows = build_group_summary_rows(full_summary)
        write_csv(group_csv_path, group_rows)

    if should_write_report:
        report = build_markdown_report(
            input_path=input_path,
            record_type=record_type,
            num_input_records=len(records),
            summary_payload=summary_payload,
            full_summary=full_summary,
        )
        report_path.write_text(report, encoding="utf-8")

    extra_written = {}
    if opt.extra_group_by:
        extra_written = write_extra_group_summaries(
            stats=stats,
            output_dir=output_dir,
            prefix=prefix,
            group_keys=opt.extra_group_by,
        )

    if opt.print_records > 0:
        flat_records = get_flat_records(
            records=records,
            record_type=record_type,
            skip_bypassed=opt.skip_bypassed,
        )
        print(f"[ARCE summarize_comm_logs] First {opt.print_records} flat records:")
        for idx, item in enumerate(flat_records[: opt.print_records]):
            print(f"--- record {idx} ---")
            print(json.dumps(json_safe(item), indent=2, ensure_ascii=False))

    if not opt.quiet:
        print_compact_summary(compact)
        print("[ARCE summarize_comm_logs] Outputs:")
        print(f"  - summary_json:      {summary_path}")
        print(f"  - full_summary_json: {full_summary_path}")

        if should_write_flat_csv:
            print(f"  - flat_csv:          {flat_csv_path}")

        if should_write_group_csv:
            print(f"  - group_csv:         {group_csv_path}")

        if should_write_report:
            print(f"  - report_md:         {report_path}")

        for key, path in extra_written.items():
            print(f"  - by_{key}:           {path}")



def ensure_dir(path: Path) -> Path:
    """
    Create directory if missing.
    """
    path.mkdir(parents=True, exist_ok=True)
    return path


def json_safe(value: Any) -> Any:
    """
    Convert values to JSON-friendly types.
    """
    if value is None:
        return None

    if isinstance(value, (str, int, bool)):
        return value

    if isinstance(value, float):
        return value if math.isfinite(value) else None

    if isinstance(value, dict):
        return {str(k): json_safe(v) for k, v in value.items()}

    if isinstance(value, (list, tuple)):
        return [json_safe(v) for v in value]

    return str(value)


def load_jsonl(path: Path) -> List[Dict[str, Any]]:
    """
    Load JSONL file.

    Parameters
    ----------
    path : Path
        JSONL file path.

    Returns
    -------
    list of dict
        Loaded records.
    """
    if not path.exists():
        raise FileNotFoundError(f"JSONL file does not exist: {path}")

    records: List[Dict[str, Any]] = []

    with path.open("r", encoding="utf-8") as f:
        for line_idx, line in enumerate(f, start=1):
            line = line.strip()

            if not line:
                continue

            try:
                item = json.loads(line)
            except json.JSONDecodeError as exc:
                raise json.JSONDecodeError(
                    f"Failed to parse {path} at line {line_idx}: {exc.msg}",
                    exc.doc,
                    exc.pos,
                )

            if not isinstance(item, dict):
                raise ValueError(
                    f"Each JSONL line should be a dict. "
                    f"Got {type(item)} at line {line_idx} in {path}."
                )

            records.append(item)

    return records


def write_json(path: Path, data: Any, indent: int = 2) -> None:
    """
    Write JSON file.
    """
    with path.open("w", encoding="utf-8") as f:
        json.dump(json_safe(data), f, indent=indent, ensure_ascii=False)


def collect_csv_fieldnames(records: Sequence[Dict[str, Any]]) -> List[str]:
    """
    Collect stable CSV fieldnames from records.
    """
    fieldnames: List[str] = []
    seen = set()

    for record in records:
        for key in record.keys():
            if key not in seen:
                seen.add(key)
                fieldnames.append(key)

    return fieldnames


def csv_value(value: Any) -> Any:
    """
    Convert value for CSV writing.
    """
    value = json_safe(value)

    if isinstance(value, (dict, list, tuple)):
        return json.dumps(value, ensure_ascii=False)

    return value


def write_csv(path: Path, records: Sequence[Dict[str, Any]]) -> None:
    """
    Write records to CSV.
    """
    records = [json_safe(r) for r in records]

    fieldnames = collect_csv_fieldnames(records)

    with path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=fieldnames,
            extrasaction="ignore",
        )
        writer.writeheader()

        for record in records:
            row = {key: csv_value(record.get(key, "")) for key in fieldnames}
            writer.writerow(row)


